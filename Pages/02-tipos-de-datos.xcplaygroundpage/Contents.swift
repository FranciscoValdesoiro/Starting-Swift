import UIKit

// String
let str: String = "string 1" // String
let str2 = "string 2" // String
let str3 = str + str2 // String

print(str3)

// Int
let int: Int = 1
let int2 = 4
let int3 = int + int2

print(int3)

// Double
let double: Double = 1.3
let double2 = 1.4
let double3 = double + double2

print(double3)

// Float  'igual que el double pero para números mas pequeños'
let float: Float = 1.3
let float2: Float = 1.4 // si no especificamos el tipo y es un numero decimal, swift por defecto lo identifica como double
let float3 = float + float2

print(float3)

// Bool
let bool: Bool = true
let bool2 = false

print(bool2)
print(bool && bool2)
print(bool == bool2)

